def _build_affinity_matrix() -> dict[tuple[TerrainType, TerrainType], float]:
    """Build the full symmetric affinity matrix from the upper triangle pairs."""
    matrix: dict[tuple[TerrainType, TerrainType], float] = {}

    # Self affinity — every terrain type strongly prefers itself
    for t in TerrainType:
        matrix[(t, t)] = 1.0

    # Fill both directions from the pairs
    for (a, b), value in _AFFINITY_PAIRS.items():
        matrix[(a, b)] = value
        matrix[(b, a)] = value

    # Any undefined pair defaults to 0.0
    for a in TerrainType:
        for b in TerrainType:
            if (a, b) not in matrix:
                matrix[(a, b)] = 0.0

    return matrix


# The global affinity matrix — built once at import time
AFFINITY_MATRIX = _build_affinity_matrix()


def get_affinity(a: TerrainType, b: TerrainType) -> float:
    """Return the affinity between two terrain types."""
    return AFFINITY_MATRIX[(a, b)]
